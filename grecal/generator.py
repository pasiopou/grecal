def select_namedays(
    namedays: Iterable[Nameday],
    *,
    top: int | None = None,
    min_popularity: int | None = None,
) -> tuple[Nameday, ...]:
    """Select entries by rank or minimum popularity; no filters means all."""

    if top is not None and min_popularity is not None:
        raise ValueError("top and min_popularity are mutually exclusive")
    if top is not None and top < 1:
        raise ValueError("top must be at least 1")
    if min_popularity is not None and not 0 <= min_popularity <= 100:
        raise ValueError("min_popularity must be between 0 and 100")

    ordered = sorted(namedays, key=lambda item: (-item.popularity, item.id))
    if top is not None:
        return tuple(ordered[:top])
    if min_popularity is not None:
        return tuple(
            item for item in ordered if item.popularity >= min_popularity
        )
    return tuple(ordered)


def _normalized_name(name: str) -> str:
    """Return a case- and tonos-insensitive key for a display name."""

    return "".join(
        character
        for character in unicodedata.normalize("NFKD", name.strip()).casefold()
        if not unicodedata.combining(character)
    )


def _edit_distance(left: str, right: str) -> int:
    """Return an adjacent-transposition-aware edit distance."""

    rows = len(left) + 1
    columns = len(right) + 1
    distances = [[0] * columns for _ in range(rows)]
    for row in range(rows):
        distances[row][0] = row
    for column in range(columns):
        distances[0][column] = column

    for row in range(1, rows):
        for column in range(1, columns):
            substitution_cost = left[row - 1] != right[column - 1]
            distances[row][column] = min(
                distances[row - 1][column] + 1,
                distances[row][column - 1] + 1,
                distances[row - 1][column - 1] + substitution_cost,
            )
            if (
                row > 1
                and column > 1
                and left[row - 1] == right[column - 2]
                and left[row - 2] == right[column - 1]
            ):
                distances[row][column] = min(
                    distances[row][column],
                    distances[row - 2][column - 2] + 1,
                )
    return distances[-1][-1]


def _fuzzy_similarity(left: str, right: str) -> float:
    longest = max(len(left), len(right))
    if longest == 0:
        return 1.0
    edit_similarity = 1 - (_edit_distance(left, right) / longest)
    sequence_similarity = SequenceMatcher(
        None,
        left,
        right,
        autojunk=False,
    ).ratio()
    return (0.65 * edit_similarity) + (0.35 * sequence_similarity)


def search_names(
    catalog: Catalog,
    query: str,
    *,
    limit: int = 10,
) -> tuple[NameSearchResult, ...]:
    """Return ranked exact, partial, and fuzzy catalog-name matches."""

    normalized_query = _normalized_name(query)
    if not normalized_query:
        raise ValueError("search query must not be empty")
    if limit < 1:
        raise ValueError("limit must be at least 1")

    ranked: list[tuple[int, float, NameSearchResult]] = []
    for nameday in catalog.namedays:
        for display_name in nameday.names:
            normalized_display_name = _normalized_name(display_name)
            if normalized_display_name == normalized_query:
                category = 4
                score = 1.0
                match_type = "exact"
            elif normalized_display_name.startswith(normalized_query):
                category = 3
                score = len(normalized_query) / len(normalized_display_name)
                match_type = "prefix"
            elif normalized_query in normalized_display_name:
                category = 2
                score = len(normalized_query) / len(normalized_display_name)
                match_type = "substring"
            else:
                if len(normalized_query) < 3:
                    continue
                score = _fuzzy_similarity(
                    normalized_query,
                    normalized_display_name,
                )
                threshold = 0.58 if len(normalized_query) <= 4 else 0.55
                if score < threshold:
                    continue
                category = 1
                match_type = "fuzzy"

            result = NameSearchResult(
                name=display_name,
                nameday_id=nameday.id,
                popularity=nameday.popularity,
                score=score,
                match_type=match_type,
            )
            ranked.append((category, score, result))

    ranked.sort(
        key=lambda item: (
            -item[0],
            -item[1],
            -item[2].popularity,
            _normalized_name(item[2].name),
            item[2].nameday_id,
        )
    )
    return tuple(item[2] for item in ranked[:limit])


def select_namedays_by_name(
    catalog: Catalog,
    names: Iterable[str],
) -> tuple[Nameday, ...]:
    """Select requested display names, preserving their catalog spellings.

    Multiple requested variants from one identity group are returned as one
    ``Nameday`` carrying only those variants. Matching ignores case and Greek
    diacritics.
    """

    index: dict[str, tuple[Nameday, str]] = {}
    for nameday in catalog.namedays:
        for display_name in nameday.names:
            index[_normalized_name(display_name)] = (nameday, display_name)

    selected: dict[str, tuple[Nameday, list[str]]] = {}
    unknown: list[str] = []
    for requested_name in names:
        cleaned_name = requested_name.strip()
        match = index.get(_normalized_name(cleaned_name)) if cleaned_name else None
        if match is None:
            if cleaned_name not in unknown:
                unknown.append(cleaned_name or "<empty>")
            continue
        nameday, display_name = match
        if nameday.id not in selected:
            selected[nameday.id] = (nameday, [])
        selected_names = selected[nameday.id][1]
        if display_name not in selected_names:
            selected_names.append(display_name)

    if unknown:
        label = "name" if len(unknown) == 1 else "names"
        raise ValueError(
            f"{label} not found in the catalog: {', '.join(unknown)}"
        )
    if not selected:
        raise ValueError("at least one name is required")

    return tuple(
        Nameday(
            id=nameday.id,
            feast=nameday.feast,
            popularity=nameday.popularity,
            names=tuple(display_names),
        )
        for nameday, display_names in selected.values()
    )


def _generate_selected_namedays(
    catalog: Catalog,
    selected: Iterable[Nameday],
    from_year: int,
    to_year: int,
    *,
    custom_rules: Mapping[str, CustomRule] | None = None,
) -> dict[date, tuple[str, ...]]:
    if not MIN_YEAR <= from_year <= to_year <= MAX_YEAR:
        raise ValueError(
            f"year range must satisfy {MIN_YEAR} <= from_year <= to_year <= {MAX_YEAR}"
        )
    feasts = catalog.feast_map()
    grouped: dict[date, list[str]] = defaultdict(list)
    for year in range(from_year, to_year + 1):
        easter = orthodox_easter(year)
        for nameday in selected:
            celebration = resolve_feast_date(
                feasts[nameday.feast],
                year,
                easter,
                custom_rules=custom_rules,
            )
            for name in nameday.names:
                if name not in grouped[celebration]:
                    grouped[celebration].append(name)
    return {day: tuple(names) for day, names in sorted(grouped.items())}


def generate_namedays(
    catalog: Catalog,
    from_year: int,
    to_year: int,
    *,
    top: int | None = None,
    min_popularity: int | None = None,
    custom_rules: Mapping[str, CustomRule] | None = None,
) -> dict[date, tuple[str, ...]]:
    """Resolve and group selected names, returning at most one group per day."""

    selected = select_namedays(
        catalog.namedays, top=top, min_popularity=min_popularity
    )
    return _generate_selected_namedays(
        catalog,
        selected,
        from_year,
        to_year,
        custom_rules=custom_rules,
    )


def generate_personal_namedays(
    catalog: Catalog,
    names: Iterable[str],
    from_year: int,
    to_year: int,
    *,
    custom_rules: Mapping[str, CustomRule] | None = None,
) -> dict[date, tuple[str, ...]]:
    """Resolve only the requested display names for a personal calendar."""

    selected = select_namedays_by_name(catalog, names)
    return _generate_selected_namedays(
        catalog,
        selected,
        from_year,
        to_year,
        custom_rules=custom_rules,
    )


def generate_observances(
    catalog: Catalog,
    from_year: int,
    to_year: int,
    *,
    custom_rules: Mapping[str, CustomRule] | None = None,
) -> dict[date, tuple[str, ...]]:
    """Resolve and group church observance titles by date."""

    if not MIN_YEAR <= from_year <= to_year <= MAX_YEAR:
        raise ValueError(
            f"year range must satisfy {MIN_YEAR} <= from_year <= to_year <= {MAX_YEAR}"
        )
    feasts = catalog.feast_map()
    grouped: dict[date, list[str]] = defaultdict(list)
    for year in range(from_year, to_year + 1):
        easter = orthodox_easter(year)
        for observance in catalog.observances:
            celebration = resolve_feast_date(
                feasts[observance.feast],
                year,
                easter,
                custom_rules=custom_rules,
            )
            if observance.title not in grouped[celebration]:
                grouped[celebration].append(observance.title)
    return {day: tuple(titles) for day, titles in sorted(grouped.items())}


def lookup_date(
    catalog: Catalog,
    day: date,
    *,
    custom_rules: Mapping[str, CustomRule] | None = None,
) -> DateLookupResult:
    """Resolve all namedays and church observances for ``day``."""

    if not MIN_YEAR <= day.year <= MAX_YEAR:
        raise ValueError(f"date year must be between {MIN_YEAR} and {MAX_YEAR}")
    namedays = generate_namedays(
        catalog,
        day.year,
        day.year,
        custom_rules=custom_rules,
    ).get(day, ())
    observances = generate_observances(
        catalog,
        day.year,
        day.year,
        custom_rules=custom_rules,
    ).get(day, ())
    return DateLookupResult(
        day=day,
        namedays=namedays,
        observances=observances,
    )


def validate_catalog(
    catalog: Catalog,
    from_year: int = MIN_YEAR,
    to_year: int = MAX_YEAR,
    *,
    custom_rules: Mapping[str, CustomRule] | None = None,
) -> None:
    """Resolve every feast rule throughout a supported year range."""

    if not MIN_YEAR <= from_year <= to_year <= MAX_YEAR:
        raise ValueError(
            f"year range must satisfy {MIN_YEAR} <= from_year <= to_year <= {MAX_YEAR}"
        )
    for year in range(from_year, to_year + 1):
        easter = orthodox_easter(year)
        for feast in catalog.feasts:
            resolve_feast_date(
                feast,
                year,
                easter,
                custom_rules=custom_rules,
            )


def _calendar_uuid(calendar_id: str) -> UUID:
    return uuid5(GRECAL_UID_NAMESPACE, calendar_id)


def _calendar_uid(calendar_id: str) -> str:
    return f"urn:uuid:{_calendar_uuid(calendar_id)}"


def _event_uid(day: date, calendar_id: str) -> str:
    return f"urn:uuid:{uuid5(_calendar_uuid(calendar_id), day.isoformat())}"


def build_calendar(
    grouped_names: Mapping[date, Sequence[str]],
    *,
    grouped_observances: Mapping[date, Sequence[str]] | None = None,
    generated_at: datetime | None = None,
    calendar_name: str | None = DEFAULT_CALENDAR_NAME,
    calendar_description: str | None = DEFAULT_CALENDAR_DESCRIPTION,
    calendar_color: str | None = None,
    calendar_id: str = DEFAULT_CALENDAR_ID,
) -> Calendar:
    """Build a UTF-8 calendar with at most one all-day event per date."""

    stamp = generated_at or datetime.now(timezone.utc)
    if stamp.tzinfo is None:
        raise ValueError("generated_at must be timezone-aware")
    cleaned_calendar_id = calendar_id.strip()
    if not cleaned_calendar_id:
        raise ValueError("calendar_id must be a non-empty string")

    metadata = {
        "calendar_name": calendar_name,
        "calendar_description": calendar_description,
        "calendar_color": calendar_color,
    }
    cleaned_metadata: dict[str, str | None] = {}
    for field, value in metadata.items():
        if value is not None and not value.strip():
            raise ValueError(f"{field} must be a non-empty string")
        cleaned_metadata[field] = value.strip() if value is not None else None

    calendar = Calendar()
    calendar.add("prodid", "-//Grecal//Greek Orthodox Calendar//EL")
    calendar.add("version", "2.0")
    calendar.add("calscale", "GREGORIAN")
    calendar.add("uid", _calendar_uid(cleaned_calendar_id))
    if cleaned_metadata["calendar_name"] is not None:
        calendar.add("name", cleaned_metadata["calendar_name"])
        calendar.add("x-wr-calname", cleaned_metadata["calendar_name"])
    if cleaned_metadata["calendar_description"] is not None:
        calendar.add("description", cleaned_metadata["calendar_description"])
        calendar.add("x-wr-caldesc", cleaned_metadata["calendar_description"])
    calendar.add("last-modified", stamp.astimezone(timezone.utc))
    if cleaned_metadata["calendar_color"] is not None:
        calendar.add("color", cleaned_metadata["calendar_color"])

    observances = grouped_observances or {}
    days = sorted(set(grouped_names) | set(observances))
    for day in days:
        names = grouped_names.get(day, ())
        titles = observances.get(day, ())
        title_text = " · ".join(titles)
        names_text = ", ".join(names)
        summary = (
            f"{title_text} — {names_text}"
            if title_text and names_text
            else title_text or names_text
        )
        event = Event()
        event.add("uid", _event_uid(day, cleaned_calendar_id))
        event.add("dtstamp", stamp.astimezone(timezone.utc))
        event.add("summary", summary)
        event.add("dtstart", day)
        event.add("dtend", day + timedelta(days=1))
        calendar.add_component(event)
    return calendar


def write_calendar(calendar: Calendar, output_path: Path) -> None:
    """Serialize an iCalendar object as UTF-8 bytes."""

    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_bytes(calendar.to_ical())


def _year_statistics(
    selected_namedays: Sequence[Nameday],
    grouped_names: Mapping[date, Sequence[str]],
    grouped_observances: Mapping[date, Sequence[str]],
    from_year: int,
    to_year: int,
    *,
    church_feast_count: int,
) -> tuple[_YearStatistics, ...]:
    identity_groups = len(selected_namedays)
    display_names = sum(len(item.names) for item in selected_namedays)
    event_days = set(grouped_names) | set(grouped_observances)
    return tuple(
        _YearStatistics(
            year=year,
            identity_groups=identity_groups,
            display_names=display_names,
            church_feasts=church_feast_count,
            calendar_events=sum(day.year == year for day in event_days),
        )
        for year in range(from_year, to_year + 1)
    )


def _selection_label(
    args: argparse.Namespace,
    selected_namedays: Sequence[Nameday],
) -> str:
    if args.feasts_only:
        return "church feasts only"
    if args.personal_names is not None:
        count = sum(len(item.names) for item in selected_namedays)
        label = f"{count} personal " + ("name" if count == 1 else "names")
    elif args.top is not None:
        label = f"top {args.top} nameday groups"
    elif args.min_popularity is not None:
        label = f"nameday popularity >= {args.min_popularity}"
    else:
        label = "all nameday groups"
    return f"{label} + church feasts" if args.include_feasts else label


def _default_calendar_id(
    args: argparse.Namespace,
    selected_namedays: Sequence[Nameday],
) -> str:
    if args.feasts_only:
        return "feasts"
    if args.personal_names is not None:
        names = sorted(
            {
                _normalized_name(name)
                for nameday in selected_namedays
                for name in nameday.names
            }
        )
        selection = f"personal:{','.join(names)}"
        return f"complete:{selection}" if args.include_feasts else selection
    if args.top is not None:
        selection = f"top:{args.top}"
    elif args.min_popularity is not None:
        selection = f"min-popularity:{args.min_popularity}"
    else:
        selection = "all"
    prefix = "complete" if args.include_feasts else "namedays"
    return f"{prefix}:{selection}"


def _default_calendar_metadata(
    args: argparse.Namespace,
    from_year: int,
    to_year: int,
) -> tuple[str, str]:
    years = (
        str(from_year) if from_year == to_year else f"{from_year}–{to_year}"
    )
    if args.feasts_only:
        name = "Grecal — Greek Orthodox Feasts"
        subject = "Greek Orthodox church feasts"
    elif args.include_feasts:
        name = DEFAULT_CALENDAR_NAME
        subject = "Greek Orthodox namedays and church feasts"
    elif args.personal_names is not None:
        name = "Grecal — Personal Namedays"
        subject = "Personal Greek Orthodox namedays"
    else:
        name = "Grecal — Greek Orthodox Namedays"
        subject = "Greek Orthodox namedays"
    return name, f"{subject} for {years}, generated by Grecal."


def _print_generation_summary(
    output_path: Path,
    selection_label: str,
    statistics: Sequence[_YearStatistics],
    *,
    dry_run_size: int | None = None,
) -> None:
    headers = (
        "Year",
        "Identity groups",
        "Display names",
        "Church feasts",
        "Calendar events",
    )
    rows: list[tuple[str, ...]] = [
        (
            str(item.year),
            str(item.identity_groups),
            str(item.display_names),
            str(item.church_feasts),
            str(item.calendar_events),
        )
        for item in statistics
    ]
    totals = tuple(
        sum(getattr(item, field) for item in statistics)
        for field in (
            "identity_groups",
            "display_names",
            "church_feasts",
            "calendar_events",
        )
    )
    rows.append(("Total occurrences", *(str(value) for value in totals)))
    if len(statistics) > 1:
        rows.append(
            (
                "Average/year",
                *(f"{value / len(statistics):.1f}" for value in totals),
            )
        )

    widths = [
        max(len(headers[index]), *(len(row[index]) for row in rows))
        for index in range(len(headers))
    ]

    def format_row(row: Sequence[str]) -> str:
        return "  ".join(
            value.ljust(widths[index])
            if index == 0
            else value.rjust(widths[index])
            for index, value in enumerate(row)
        )

    years = (
        str(statistics[0].year)
        if len(statistics) == 1
        else f"{statistics[0].year}-{statistics[-1].year}"
    )
    if dry_run_size is None:
        print(f"Generated: {output_path} ({output_path.stat().st_size:,} bytes)")
    else:
        print("Dry run: no file written")
        print(f"Would generate: {output_path} ({dry_run_size:,} bytes)")
    print(f"Years: {years}")
    print(f"Selection: {selection_label}")
    print()
    print(format_row(headers))
    print(format_row(tuple("-" * width for width in widths)))
    for row in rows:
        print(format_row(row))


