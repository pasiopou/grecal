def _generate_main(argv: Sequence[str]) -> int:
    parser = _generation_argument_parser()
    args = parser.parse_args(argv)
    if args.feasts_only and args.include_feasts:
        parser.error("--feasts-only cannot be combined with --include-feasts")
    to_year = args.to_year if args.to_year is not None else args.from_year
    try:
        catalog = _load_catalog_from_args(args)
        requested_names = (
            args.personal_names.split(",")
            if args.personal_names is not None
            else None
        )
        if args.feasts_only:
            selected_namedays = ()
            grouped_names = {}
        elif requested_names is not None:
            selected_namedays = select_namedays_by_name(
                catalog, requested_names
            )
            grouped_names = _generate_selected_namedays(
                catalog,
                selected_namedays,
                args.from_year,
                to_year,
            )
        else:
            selected_namedays = select_namedays(
                catalog.namedays,
                top=args.top,
                min_popularity=args.min_popularity,
            )
            grouped_names = generate_namedays(
                catalog,
                args.from_year,
                to_year,
                top=args.top,
                min_popularity=args.min_popularity,
            )
        grouped_observances = (
            generate_observances(catalog, args.from_year, to_year)
            if args.include_feasts or args.feasts_only
            else {}
        )
        default_calendar_name, default_calendar_description = (
            _default_calendar_metadata(args, args.from_year, to_year)
        )
        calendar_id = (
            _default_calendar_id(args, selected_namedays)
            if args.calendar_id is None
            else args.calendar_id
        )
        calendar = build_calendar(
            grouped_names,
            grouped_observances=grouped_observances,
            calendar_name=(
                default_calendar_name
                if args.calendar_name is None
                else args.calendar_name
            ),
            calendar_description=(
                default_calendar_description
                if args.calendar_description is None
                else args.calendar_description
            ),
            calendar_color=args.calendar_color,
            calendar_id=calendar_id,
        )
        dry_run_size = len(calendar.to_ical()) if args.dry_run else None
        if not args.dry_run:
            write_calendar(calendar, args.output)
        statistics = _year_statistics(
            selected_namedays,
            grouped_names,
            grouped_observances,
            args.from_year,
            to_year,
            church_feast_count=(
                len(catalog.observances)
                if args.include_feasts or args.feasts_only
                else 0
            ),
        )
        _print_generation_summary(
            args.output,
            _selection_label(args, selected_namedays),
            statistics,
            dry_run_size=dry_run_size,
        )
    except (
        DataValidationError,
        OSError,
        UnsupportedFeastRuleError,
        ValueError,
    ) as exc:
        parser.exit(2, f"{parser.prog}: error: {exc}\n")
    return 0


def _find_main(argv: Sequence[str]) -> int:
    parser = _find_argument_parser()
    args = parser.parse_args(argv)
    to_year = args.to_year if args.to_year is not None else args.from_year
    try:
        catalog = _load_catalog_from_args(args)
        try:
            selected_namedays = select_namedays_by_name(catalog, (args.name,))
        except ValueError as exc:
            command = f"grecal search {shlex.quote(args.name)}"
            parser.exit(
                2,
                f"{parser.prog}: error: {exc}\nTry: {command}\n",
            )
        grouped_names = _generate_selected_namedays(
            catalog,
            selected_namedays,
            args.from_year,
            to_year,
        )
        selected = selected_namedays[0]
        source_nameday = next(
            item for item in catalog.namedays if item.id == selected.id
        )
    except (
        DataValidationError,
        OSError,
        UnsupportedFeastRuleError,
        ValueError,
    ) as exc:
        parser.exit(2, f"{parser.prog}: error: {exc}\n")

    print(f"Name: {selected.names[0]}")
    print(f"Identity group: {selected.id}")
    print(f"Variants: {', '.join(source_nameday.names)}")
    print("Dates:")
    for day in grouped_names:
        print(f"  {day.isoformat()}")
    return 0


def _search_main(argv: Sequence[str]) -> int:
    parser = _search_argument_parser()
    args = parser.parse_args(argv)
    try:
        catalog = _load_catalog_from_args(args)
        results = search_names(catalog, args.query, limit=args.limit)
    except (
        DataValidationError,
        OSError,
        ValueError,
    ) as exc:
        parser.exit(2, f"{parser.prog}: error: {exc}\n")

    if not results:
        print(f"No matching names found for: {args.query}")
        return 1
    for result in results:
        print(f"{result.name}  group: {result.nameday_id}")
    return 0


def _date_main(argv: Sequence[str]) -> int:
    parser = _date_argument_parser()
    args = parser.parse_args(argv)
    try:
        catalog = _load_catalog_from_args(args)
        result = lookup_date(catalog, args.day)
    except (
        DataValidationError,
        OSError,
        UnsupportedFeastRuleError,
        ValueError,
    ) as exc:
        parser.exit(2, f"{parser.prog}: error: {exc}\n")

    print(f"Date: {result.day.isoformat()}")
    print(f"Namedays: {', '.join(result.namedays) if result.namedays else 'none'}")
    print(
        "Church observances: "
        + (", ".join(result.observances) if result.observances else "none")
    )
    return 0 if result.namedays or result.observances else 1


def main(argv: Sequence[str] | None = None) -> int:
    arguments = tuple(sys.argv[1:] if argv is None else argv)
    if not arguments:
        _main_argument_parser().print_help()
        return 0

    command_handlers = {
        "generate": _generate_main,
        "find": _find_main,
        "search": _search_main,
        "date": _date_main,
        "validate": _validate_main,
    }
    handler = command_handlers.get(arguments[0])
    if handler is not None:
        return handler(arguments[1:])

    parser = _main_argument_parser()
    if arguments[0].startswith("-") and arguments[0] not in {
        "-h",
        "--help",
        "--version",
    }:
        parser.error(
            f"unrecognized argument: {arguments[0]}; "
            "generation options require 'grecal generate'"
        )
    parser.parse_args(arguments)
    return 0
